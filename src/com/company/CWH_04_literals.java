package com.company;

public class CWH_04_literals {
    public static void main(String[] args) {
        // A literal is a constant value which can be assigned to a variable, hence making a variable immutable
        byte age = 34;
        // Character literals can be declared only by using single quotes
        char ch = 'A';
        // Floating point literals can be declared by adding an 'f' at the end of the value
        float f1 = 5.6f ;
        // Double literals are directly declared, but can also be declared by using a 'd' at the end of the value
        double d1 = 6.1d;
        boolean a = true;
        // Java has special support for string literals. String literals can be declared using double quotes
        String str = "Almadi";


    }
}
