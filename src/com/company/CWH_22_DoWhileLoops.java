package com.company;

public class CWH_22_DoWhileLoops {
    public static void main(String[] args) {
//        Do while Loops are similar to while loops except the guarantee to run at least once. A do while loops first executes the statement and then
//        checks the condition.
        int b = 10;
        do {
            System.out.println(b);
            b++;
        }while(b<5);


    }
}
