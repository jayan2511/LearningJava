package com.company;

public class CWH_16_IfElseStatements {
    public static void main(String[] args) {
//        Java has 2 types of conditional statements
//              1. IF ELSE STATEMENTS
//              2. SWITCH CASE STATEMENTS
        int age = 19;


//        SYNTAX OF IF ELSE STATEMENT:
//        if (condition){
//          statement if condition true
//        }
//        else-if(condition){
//          statement if condition true
//        }
//        else{
//          statement if all conditions false
//        }



        if (age>=18){
            System.out.println("Yes boy you can drive");
        }
        else{
            System.out.println("No boy you cannot drive");
        }



    }
}
